create table if not exists public.statuses (
  id bigint generated by default as identity primary key,
  name text not null unique,
  color text not null default '#667085',
  sort_order integer not null default 100,
  is_active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.tags (
  id bigint generated by default as identity primary key,
  name text not null,
  created_at timestamptz not null default now()
);

create unique index if not exists tags_name_lower_uidx on public.tags (lower(name));

create table if not exists public.request_tags (
  request_id bigint not null references public.requests(id) on update cascade on delete cascade,
  tag_id bigint not null references public.tags(id) on update cascade on delete cascade,
  created_at timestamptz not null default now(),
  primary key (request_id, tag_id)
);

create index if not exists request_tags_tag_id_idx on public.request_tags(tag_id);

