-- Переносим старое качество в новый статус только там, где status_id ещё не задан.
update public.requests r
set legacy_quality = coalesce(r.legacy_quality, r.quality),
    status_id = s.id
from public.statuses s
where r.status_id is null
  and s.name = case
    when r.quality in ('Готово', 'Можно создавать') then 'Обработать'
    when r.quality in ('Нужно уточнить', 'Недостаточно контекста', 'Требует разделения', 'Почти готово', 'Готово после уточнений', 'Уточнить', 'Не готово') then 'Нужно уточнить'
    else 'Новый'
  end;

